    private void CadastrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarBtnActionPerformed
        // TODO add your handling code here:
        String nome = CadNome.getText();
        String numero = CadNum.getText();
        String time = CadTime.getText();
        
        int num = Integer.parseInt(numero);
        int timeConv = Integer.parseInt(time);
        
        if (nome == null){ 
            JOptionPane.showMessageDialog (null, "Preencha os dados"); 
        } 
        else{ 
            try{ 
                int escolha = JOptionPane.showConfirmDialog(null, "Confirmar cadastro" 
                + " de novo Player?"); 
                if (escolha == JOptionPane.YES_OPTION){ 
                    Players players = new Players(timeConv, nome, num); 
                    DAO dao = new DAO(); 
                    dao.inserirPlayers(players); 
                    JOptionPane.showMessageDialog(null, "Player cadastrado com" + 
                        "sucesso"); 
                    CadNome.setText("");
                    CadNum.setText("");
                    CadTime.setText("");
                }  
            } 
            catch (Exception e){ 
                JOptionPane.showMessageDialog(null, "Falha t√©cnica, tente mais tarde"); 
            }
        }
    }//GEN-LAST:event_CadastrarBtnActionPerformed

    private void AtualizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) ListPlayers.getModel();
        model.setRowCount(0);
        DAO dao = new DAO();
       
        try {
            int quantPlayers = dao.quantPlayers();
            
            for (int i = 0; i < quantPlayers; i++) {
                model.addRow(new String[]{dao.obterPlayers()[i].getNome()});
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ListUsers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AtualizarBtnActionPerformed

